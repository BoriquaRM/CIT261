function mouseOn() {
	document.getElementById("imgTDjs").style.border = "2px solid red";
}
function mouseOff() {
	document.getElementById("imgTDjs").style.border = "none";
}

function changeSize() {
	document.getElementById("imgTDjs").style.width = "180%";
}

function changeBorder() {
	document.getElementById("imgTDjs").style.borderRadius = "50px";
}

function normal() {
	document.getElementById("imgTDjs").style.width = "77%"
	document.getElementById("imgTDjs").style.border = "none"
	document.getElementById("imgTDjs").style.borderRadius = "0px";
}